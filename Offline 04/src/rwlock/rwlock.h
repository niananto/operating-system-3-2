{
    int totalreaders;
    int writerlock;
    int writerswaiting;
    pthread_mutex_t mutexvar;
    pthread_cond_t writercond;
    pthread_cond_t readercond;
    
};

void InitalizeReadWriteLock(struct read_write_lock * rw);
